# Operadores de atribuição
# São operadores que atribuem valores a variáveis
# Exemplo: saldo = 1000

saldo = 2500 # saldo = 2500
print(saldo)    # Exibe o valor de saldo na tela
# saldo = 2500

saldo = 200 # saldo = 200
print(saldo)    # Exibe o valor de saldo na tela

saldo += 10 # saldo = 200 + 10
print(saldo)   # Exibe o valor de saldo na tela

saldo -= 5 # saldo = 200 - 5
print(saldo)    # Exibe o valor de saldo na tela

saldo //= 2 # saldo = 200 // 2
print(saldo)   # Exibe o valor de saldo na tela

saldo /= 4  # saldo = 200 / 4
print(saldo)   # Exibe o valor de saldo na tela

saldo *= 100 # saldo = 200 * 100
print(saldo)   # Exibe o valor de saldo na tela

saldo %= 4  # saldo = 200 % 4
print(saldo)    # Exibe o valor de saldo na tela

saldo **= 2 # saldo = 200 ** 2
print(saldo)    # Exibe o valor de saldo na tela